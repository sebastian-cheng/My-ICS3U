package day5;

public class PracticeTwo {
    public static void main(String[] args) {
        double length = 4.5; 
        double width = 2.3; 

        double perimeter = (length * 2) + (width * 2);
        double area = length * width; 
       
        System.out.println("The perimeter of a rectangle with a length of 4.5 feet and a width of 2.3 feet is " + perimeter + " feet");
        System.out.println("The area of a rectangle with a length of 4.5 feet and a width of 2.3 feet is " + area + " feet");
        
    }
}
